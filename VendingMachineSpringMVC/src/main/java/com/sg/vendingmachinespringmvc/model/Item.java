package com.sg.vendingmachinespringmvc.model;

import java.math.BigDecimal;

/**
 *
 * @author jono
 */
public class Item {

    private String name;
    private BigDecimal price;
    private int qty;
    private int id;

    public Item(int id, String name, BigDecimal price, int qty) {
        this.id = id;
        this.name = name;
        this.price = price;

        this.qty = qty;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public BigDecimal getPrice() {
        return price;
    }

    public void setPrice(BigDecimal price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
